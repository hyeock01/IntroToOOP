package basics;

public class DataTypes {
    public static void main(String[] args) {
        byte a = 10; // 8비트정수 -128 ~ 127
        short b = 10; // 16비트정수 -2^14 ~ 2^14-1
        int c = 10; // 32비트정수 -2^30 ~ 2^30-1
        long d = 10; // 64비트정수 -2^62 ~ 2^62-1

        char e = 71; // 16비트 - a
        boolean f = false; // T/F
        float g = 3.14f; // 32비트실수
        double h = 3.14; // 64비트실수

//      +-*/% 연산자
//      += -= *= /= %= 대입연산자
//      <>==<=>=!= 비교연산자
//      && = and 논
//      || = or
//      !  = not 리
//      ++,-- 증가감소
    }
}
